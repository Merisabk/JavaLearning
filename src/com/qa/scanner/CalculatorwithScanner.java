package com.qa.scanner;


public class CalculatorwithScanner {
	
	public int add(int a, int b) {
		return a + b;
		
	}
	
	public int sub(int a, int b) {
		return a - b;
		
	}
	public int mul(int a, int b) {
		return a * b;
		
	}
	
	
	public double div(int a, int b) {
			double x = a;
			double y = b;
			return x/y;
		}
	}






